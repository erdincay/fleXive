package com.flexive.shared.exceptions;

/**
 * Exception thrown from invalid query nodes. Contains the query node ID, useful for
 * marking the error tree node in the UI.
 *
 * @author Daniel Lichtenberger (daniel.lichtenberger@flexive.com), UCS - unique computing solutions gmbh (http://www.ucs.at)
 * @version $Rev$
 */
public class FxInvalidQueryNodeException extends FxApplicationException {
    private static final long serialVersionUID = 2909151453566116614L;
    private final int treeNodeId;

    public FxInvalidQueryNodeException(int treeNodeId, FxApplicationException converted) {
        super(converted);
        this.treeNodeId = treeNodeId;
    }

    public FxInvalidQueryNodeException(int treeNodeId, String key, Object... values) {
        super(key, values);
        this.treeNodeId = treeNodeId;
    }

    public int getTreeNodeId() {
        return treeNodeId;
    }
}
