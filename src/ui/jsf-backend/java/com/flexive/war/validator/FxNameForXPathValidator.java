package com.flexive.war.validator;

import com.flexive.faces.messages.FxFacesMsgErr;

import javax.faces.component.UIComponent;
import javax.faces.context.FacesContext;
import javax.faces.validator.Validator;
import javax.faces.validator.ValidatorException;
import java.util.regex.Matcher;
import java.util.regex.Pattern;

/**
 * Validates if a name corresponds to the [A-Z][A-Z_0-9] pattern,
 * so that the name can be used to build a valid XPath
 *
 * @author Gerhard Glos (gerhard.glos@flexive.com), UCS - unique computing solutions gmbh (http://www.ucs.at)
 */

public class FxNameForXPathValidator implements Validator {

    private static Pattern namePattern = Pattern.compile("[a-zA-Z][a-zA-Z_0-9]*");

    public void validate(FacesContext context, UIComponent component, Object value) throws ValidatorException {
        if (value != null) {
            String name = (String) value;
            Matcher m = namePattern.matcher(name);
            if (m.matches())
                return; //all correct
        }
        throw new ValidatorException(new FxFacesMsgErr("FxValueInput.err.invalidNameForXPath"));
    }
}
